class Person {
    private String name;
    private String dateOfBirth;

    public void setName(String n){
        name = n;
    }
    public void setDateOfBirth(String dob){
        dateOfBirth = dob;
    }

    public String getName(){
        return name;
    }
    public String getDateOfBirth(){
        return dateOfBirth;
    }
}
